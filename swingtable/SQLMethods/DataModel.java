package swingtable.SQLMethods;

public class DataModel {
    String id;
    String name, email, ph, pasword, gender;

    public DataModel(String id, String name, String email, String ph, String pasword, String gender) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.ph = ph;
        this.pasword = pasword;
        this.gender = gender;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPh() {
        return ph;
    }

    public void setPh(String ph) {
        this.ph = ph;
    }

    public String getPasword() {
        return pasword;
    }

    public void setPasword(String pasword) {
        this.pasword = pasword;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    
    
    
}
